def parse_pot(path: Path) -> list[tuple[list[str], str]]:
    """Parse a .pot file. Returns list of (comment_lines, msgid) tuples in order.

    `msgid` is the raw quoted-string payload (newline-joined for multi-line);
    comment_lines is every `#.`, `#:`, `#,` line preceding the msgid. The
    empty-msgid header is dropped — we emit our own.
    """
    entries: list[tuple[list[str], str]] = []
    comment: list[str] = []
    current_msgid: list[str] | None = None
    reading_msgid = False
    reading_msgstr = False

    for line in path.read_text().splitlines():
        if line.startswith("#"):
            if not reading_msgid and not reading_msgstr:
                comment.append(line)
            continue
        if line.startswith("msgid "):
            # flush previous entry if any
            if current_msgid is not None and "".join(current_msgid):
                # Concatenate without inserting newlines — continuation lines
                # are payload continuations, not separators. Actual newlines
                # come from \n escapes inside the payload (decoded below).
                entries.append((comment, _unescape("".join(current_msgid))))
            current_msgid = [_strip_quotes(line[len("msgid ") :])]
            reading_msgid = True
            reading_msgstr = False
            comment = []  # reset for next block
            continue
        if line.startswith("msgstr "):
            reading_msgid = False
            reading_msgstr = True
            continue
        if line.startswith('"'):
            if reading_msgid and current_msgid is not None:
                current_msgid.append(_strip_quotes(line))
            # ignore msgstr continuation
            continue
        if line.strip() == "":
            # blank line ends the current entry
            if current_msgid is not None and "".join(current_msgid):
                # Concatenate without inserting newlines — continuation lines
                # are payload continuations, not separators. Actual newlines
                # come from \n escapes inside the payload (decoded below).
                entries.append((comment, _unescape("".join(current_msgid))))
                current_msgid = None
                comment = []
            reading_msgid = False
            reading_msgstr = False

    # tail
    if current_msgid is not None and "".join(current_msgid):
        entries.append((comment, _unescape("\n".join(current_msgid))))
    return entries


def _strip_quotes(raw: str) -> str:
    raw = raw.strip()
    if raw.startswith('"') and raw.endswith('"'):
        return raw[1:-1]
    return raw


def _unescape(s: str) -> str:
    """Decode the tiny subset of escapes that appear in .pot msgids.

    Handled: \\n, \\t, \\", \\\\. Everything else is kept verbatim so non-
    ASCII UTF-8 (ellipsis, em-dash, accented letters, …) survives intact.
    Don't use codecs.decode('unicode_escape') — it double-decodes UTF-8
    byte sequences as Latin-1 escapes.
    """
    out: list[str] = []
    i = 0
    while i < len(s):
        if s[i] == "\\" and i + 1 < len(s):
            nxt = s[i + 1]
            if nxt == "n":
                out.append("\n")
            elif nxt == "t":
                out.append("\t")
            elif nxt == '"':
                out.append('"')
            elif nxt == "\\":
                out.append("\\")
            else:
                out.append(s[i : i + 2])
            i += 2
        else:
            out.append(s[i])
            i += 1
    return "".join(out)


def _escape(s: str) -> str:
    """Prepare a string for a .po quoted literal.

    .po files are UTF-8; only backslash / double-quote / tab need escaping.
    Newlines are handled upstream (by splitting the string on \n). Using
    `unicode_escape` here would corrupt non-ASCII text (msgfmt sees the
    resulting `\\uXXXX` as "invalid control sequence").
    """
    return s.replace("\\", "\\\\").replace('"', '\\"').replace("\t", "\\t")


def format_msgid(s: str) -> list[str]:
    """Split a string into .po msgid lines (multi-line if it contains \\n)."""
    if "\n" not in s:
        return [f'msgid "{_escape(s)}"']
    # Multi-line: first line is `msgid ""` then each line is its own quoted string.
    lines = s.split("\n")
    out = ['msgid ""']
    for i, seg in enumerate(lines):
        suffix = "\\n" if i < len(lines) - 1 else ""
        out.append(f'"{_escape(seg)}{suffix}"')
    return out


def format_msgstr(s: str) -> list[str]:
    if not s:
        return ['msgstr ""']
    if "\n" not in s:
        return [f'msgstr "{_escape(s)}"']
    lines = s.split("\n")
    out = ['msgstr ""']
    for i, seg in enumerate(lines):
        suffix = "\\n" if i < len(lines) - 1 else ""
        out.append(f'"{_escape(seg)}{suffix}"')
    return out


def build_po(
    pot_path: Path,
    lang_code: str,
    lang_name: str,
    plural_forms: str,
    translations: dict[str, str],
) -> str:
    """Build a .po file as text from the .pot + a translation dict."""
    entries = parse_pot(pot_path)
    lines: list[str] = [
        f"# Translation of the pdfgeneratorapi_connector addon — {lang_name}.",
        "# Copyright (C) 2026 PDF Generator API",
        "# Generated by scripts/i18n_translate.py — edit there, not here.",
        "#",
        'msgid ""',
        'msgstr ""',
        '"Project-Id-Version: pdfgeneratorapi_connector 19.0.1.0.0\\n"',
        '"Report-Msgid-Bugs-To: \\n"',
        '"PO-Revision-Date: 2026-04-24 00:00+0000\\n"',
        '"Last-Translator: PDF Generator API <support@pdfgeneratorapi.com>\\n"',
        f'"Language-Team: {lang_name}\\n"',
        f'"Language: {lang_code}\\n"',
        '"MIME-Version: 1.0\\n"',
        '"Content-Type: text/plain; charset=UTF-8\\n"',
        '"Content-Transfer-Encoding: 8bit\\n"',
        f'"Plural-Forms: {plural_forms}\\n"',
        "",
    ]
    for comment, msgid in entries:
        for c in comment:
            lines.append(c)
        lines.extend(format_msgid(msgid))
        lines.extend(format_msgstr(translations.get(msgid, "")))
        lines.append("")
    return "\n".join(lines) + "\n"


def main() -> int:
    wrote = 0
    for addon in ADDONS:
        pot = ADDONS_ROOT / addon / "i18n" / f"{addon}.pot"
        if not pot.exists():
            print(f"WARN: no .pot for {addon} at {pot}", file=sys.stderr)
            continue
        for lang_code, (lang_name, plural) in LANGUAGES.items():
            po = ADDONS_ROOT / addon / "i18n" / f"{lang_code}.po"
            # Main addon uses TRANSLATIONS directly. Bridges inherit every
            # main-addon translation (field labels from the mixin, standard
            # Odoo labels like ID / Display Name appear in every bridge's .pot
            # too) and override/add with their bridge-specific slice.
            translations: dict[str, str] = {}
            if addon == "pdfgeneratorapi_connector":
                translations.update(TRANSLATIONS.get(lang_code, {}))
            else:
                translations.update(TRANSLATIONS.get(lang_code, {}))
                translations.update(BRIDGE_TRANSLATIONS.get(addon, {}).get(lang_code, {}))
            po.write_text(build_po(pot, lang_code, lang_name, plural, translations))
            wrote += 1
    print(f"wrote {wrote} .po files", file=sys.stderr)
    return 0


if __name__ == "__main__":
    sys.exit(main())
