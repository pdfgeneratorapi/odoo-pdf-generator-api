"""Generate fully-translated .po files from .pot templates.

Reads `<addon>/i18n/<addon>.pot` for every addon in ADDONS and writes
`<addon>/i18n/<lang>.po` for every language in LANGUAGES, applying
translations from TRANSLATIONS[lang].

Missing translations fall back to the English msgid (gettext convention
for empty msgstr). Run this after `make i18n-export` regenerates the
`.pot` files — new strings land in the dicts below, re-run this script,
re-run msgfmt.

Usage:
    python scripts/i18n_translate.py

Verify:
    make i18n-check      # runs msgfmt -cv on every .po
"""

